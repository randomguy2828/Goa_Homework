def hello_world():
    return "Hello world!"

def hello_person(name):
    return f"Hello {name}!"

def multiply(a, b):
    return a * b

def sum_three_numbers(a, b, c):
    return a + b + c

def is_adult(age):
    if age >= 18:
        return "legal"
    else:
        return "illegal"

def did_pass(score):
    if score > 80:
        return "passed"
    else:
        return "didn't pass"

